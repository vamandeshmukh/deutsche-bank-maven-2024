package com.db.demo.design.patterns;

//DesignPatterns 
//
//Creational Patterns
//Focus on the process of object creation.
//Aim to abstract the instantiation process, 
//making it more flexible and reusable.
//
//Singleton Pattern: Ensures a class has only one instance and provides a global point of access to it.
//Factory Pattern: Defines an interface for creating objects, but allows subclasses to alter the type of objects that will be created.
//Builder Pattern: Separates the construction of a complex object from its representation, allowing the same construction process to create different representations.

//Structural Patterns
//
//Deal with object composition and the relationship between objects.
//Focus on how classes and objects can be combined to form larger structures.
//
//Bridge Pattern: Decouples an abstraction from its implementation so that the two can vary independently.
//Decorator Pattern: Adds additional responsibilities to an object dynamically.
//Proxy Pattern:  Provides a surrogate or placeholder for another object to control access to it.

//Behavioral Patterns
//
//Focus on communication between objects and the responsibilities of objects.
//Deal with the interaction and responsibilities of objects.
//
//Template Pattern: Defines the skeleton of an algorithm in the base class but 
//lets subclasses override specific steps without changing the algorithm's structure.
//Command Pattern: Encapsulates a request as an object, thereby allowing for parameterization of clients with queues, requests, and operations.
//Strategy Pattern: Defines a family of algorithms, encapsulates each one, and makes them interchangeable.


